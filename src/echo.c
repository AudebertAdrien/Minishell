#include "minishell.h"

int	echo(char **tab)
{
	int	i;

	i = 1;
	while (tab[i])
	{
		ft_putstr_fd(tab[i], 1);
		ft_putchar_fd('\n', 1);
		i++;
	}
	return (0);
}
